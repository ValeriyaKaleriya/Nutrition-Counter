package edu.beuth.nutrition.model;

public class Ingredient {

    private String name;

    private Detail detail;

    public String getName() {
        return name;
    }

    public Detail getDetail() {
        return detail;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setDetail(Detail detail) {
        this.detail = detail;
    }
}
